public class MillisecondValueConstant {
    /**
     * 一秒的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_SECOND = 1000L;
    /**
     * 一分钟的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_MINUTE = 60 * MILLISECOND_VALUE_OF_A_SECOND;
    /**
     * 一小时的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_AN_HOUR = 3600 * MILLISECOND_VALUE_OF_A_SECOND;
    /**
     * 一天的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_DAY = 86400 * MILLISECOND_VALUE_OF_A_SECOND;
    /**
     * 一周的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_WEEK = 7 * MILLISECOND_VALUE_OF_A_DAY;
    /**
     * 半个月的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_HALF_MONTH = 15 * MILLISECOND_VALUE_OF_A_DAY;
    /**
     * 一个月的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_MONTH = 30 * MILLISECOND_VALUE_OF_A_DAY;
    /**
     * 一个季度的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_QUARTER = 3 * MILLISECOND_VALUE_OF_A_MONTH;
    /**
     * 半年的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_HALF_YEAR = 6 * MILLISECOND_VALUE_OF_A_MONTH;
    /**
     * 一年的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_YEAR = 365 * MILLISECOND_VALUE_OF_A_DAY;
    /**
     * 两年的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_DOUBLE_YEAR = 2 * MILLISECOND_VALUE_OF_A_YEAR;
    /**
     * 三年的毫秒值
     */
    public static final long MILLISECOND_VALUE_OF_A_TRIPLE_YEAR = 3 * MILLISECOND_VALUE_OF_A_YEAR;
}
